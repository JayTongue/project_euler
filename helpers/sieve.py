from numba import jit

@jit
def bool_sieve(num):
    prime = [True for i in range(num+1)]
    p = 2
    while (p * p <= num):
        if (prime[p] == True):
            for i in range(p * p, num+1, p):
                prime[i] = False
        p += 1
    prime[0], prime[1] = False, False
    return prime